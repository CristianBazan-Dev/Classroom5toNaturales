                <p class="text-paragraph">
                    <b style="color:#833d0b">VII. Tiempo</b>
                </p>
                <br>
                <p class="text-paragraph ">
                    Este aspecto suele ser el más complejo. A grandes rasgos, se observará una tendencia al “spoiler” (adelantos) y al “flashback” (regresiones): se adelanta a los acontecimientos y se suele ir al pasado a revisar las vivencias.  
                </p>
                <br>
                <p class="text-paragraph ">
                    Encontramos <b>cuatro tipos de tiempos.</b>
                </p>
                <br>
                <p class="text-paragraph ">
                    1.	<i>Tiempo cronológico:</i> Las acciones siguen el curso lógico del tiempo. 
                </p>
                <br>
                <p class="text-paragraph ">
                    2.	<i>Ruptura de planos temporales:</i> mezcla de tiempo presente con tiempo pasado (regresiones) y tiempo futuro (adelantos). Además, se fragmenta el texto en secuencias que no concuerdan en tiempo ni espacio.  
                </p>
                <br>
                <p class="text-paragraph ">
                    3.	<i>Tiempo estático:</i> El tiempo cronológico se detiene, es como si no trascendiera. En cambio, fluyen los pensamientos de los personajes. 
                </p>
                <br>
                <p class="text-paragraph ">
                    4.	<i>Tiempo invertido:</i> Es el más contradictorio. Se trastoca el curso del tiempo y se cambia la secuencia natural del día hacia la noche o viceversa. Por ejemplo: "Era el amanecer. Se hizo la noche".  
                </p>
                <br>
                <p class="text-paragraph quote">
                    "Muchos años después, frente al pelotón de fusilamiento, el coronel Aureliano Buendía había de recordar aquella tarde remota en que su padre lo llevó a conocer el hielo. "
                    <br><br><div class="text-paragraph quote source">Cien años de soledad – Gabriel García Márquez</div>
